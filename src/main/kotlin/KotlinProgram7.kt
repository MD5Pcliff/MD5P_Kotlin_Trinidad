fun main(){
    //increment ++ and decerement operators --
    var x :Int = 1
    println(x++)
    //pre increment x++ post increment ++x

    var y :Int = 5
    //pre decrement --y post decrement y--
    println(--y)

    //increment specific number +=number of incerement , decrement -=number of decrement
